'''
Ejercicio No.3 Color Rojo

Problema:
¿Cuál es el conjunto resultante de la operación A-B si A = {a, 2, c, 3, e, 4, g, 5, i, 6, k, 7, m} y B = {a, b, c, d, e, f, g, h, i}
'''

#Solución
A = {'a', '2', 'c', '3', 'e', '4', 'g', '5', 'i', '6', 'k', '7', 'm'}
B = {'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i'}
resultado = A - B
print("Conjunto A: ", A)
print("Conjunto B: ", B)
print(f"Conjunto resultante de A - B: {resultado}")
